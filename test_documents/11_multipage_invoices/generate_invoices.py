def generate_invoice(idx, vendor, client, out_dir, num_items):
    """Generate a single multi-page invoice PDF."""

    inv_num   = f"{vendor['inv_prefix']}-{random.randint(1000, 9999)}"
    inv_date  = rand_date_in_2024()
    due_date  = inv_date + timedelta(days=30)
    po_number = f"PO-{random.randint(10000, 99999)}"

    filename = (f"invoice_{idx:03d}_{vendor['slug']}_{inv_num}.pdf")
    filepath = os.path.join(out_dir, filename)

    brand_color = PALETTE[idx - 1]

    # ── Build line items ─────────────────────────────────────────────────────
    pool = LINE_ITEM_POOLS[vendor["category"]]
    # Allow repetition when num_items exceeds pool size
    # Use all pool items first, then fill remainder with repeats (different qty/price)
    base_chosen = random.sample(pool, min(num_items, len(pool)))
    remainder   = num_items - len(base_chosen)
    extras      = random.choices(pool, k=remainder) if remainder > 0 else []
    chosen_raw  = base_chosen + extras
    random.shuffle(chosen_raw)

    line_items = []
    for desc, unit_price, unit in chosen_raw:
        qty      = rand_qty(unit)
        # Slight price variation ±15%
        price    = round(unit_price * random.uniform(0.85, 1.15), 2)
        ext      = round(qty * price, 2)
        line_items.append((desc, unit, qty, price, ext))

    subtotal    = sum(li[4] for li in line_items)
    tax_rate    = random.choice([0.0525, 0.06, 0.0725, 0.08, 0.085, 0.09, 0.095])
    tax_amount  = round(subtotal * tax_rate, 2)
    total       = round(subtotal + tax_amount, 2)

