def main():
    out_dir = "/Users/martinmashalov/Downloads/GridPull/test_documents/11_multipage_invoices"
    os.makedirs(out_dir, exist_ok=True)

    # Item counts calibrated for 0.42in row height:
    # Page 1 overhead ~2.8in → ~13 data rows; continuation pages ~20 data rows each
    # 3 pages → ~33-53 items; 4p → ~54-73; 5p → ~74-93; 6p → ~94-113;
