def rand_qty(unit):
    """Return a realistic quantity based on unit type."""
    unit_upper = unit.upper()
    if unit_upper in ("HR", "DY"):
        return round(random.uniform(4, 120), 1)
    elif unit_upper in ("LF", "SF", "AC", "LI", "LB"):
        return round(random.uniform(10, 500), 0)
    elif unit_upper in ("MO", "YR"):
        return random.randint(1, 12)
    elif unit_upper in ("EA", "SH", "BX", "CS", "BT", "BG", "BN"):
        return random.randint(1, 25)
    else:
        return random.randint(1, 20)

def fmt_money(amount):
    return f"${amount:,.2f}"

def rand_date_in_2024():
    """Return a random invoice date in 2024."""
    start = date(2024, 1, 1)
    end   = date(2024, 12, 15)
    delta = (end - start).days
    return start + timedelta(days=random.randint(0, delta))

